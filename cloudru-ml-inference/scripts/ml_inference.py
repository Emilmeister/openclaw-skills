def is_service_unavailable(response: httpx.Response) -> bool:
    return response.status_code in (502, 503, 504)


def get_env(name: str) -> str:
    val = os.environ.get(name)
    if not val:
        print(f"Error: environment variable {name} is not set", file=sys.stderr)
        sys.exit(1)
    return val


def build_clients():
    key_id = get_env("CP_CONSOLE_KEY_ID")
    key_secret = get_env("CP_CONSOLE_SECRET")
    project_id = UUID(get_env("PROJECT_ID"))

    iam_client = IAM_HttpClient("https://iam.api.cloud.ru/")
    auth = IAM_Auth(key_id=key_id, key_secret=key_secret, iam_client=iam_client)

    bff_client = ModelRunUsersApiClient(
        "https://console.cloud.ru",
        options.with_auth(auth),
        options.with_autogenerated_request_id(),
        options.with_autogenerated_traceparent(),
        with_retries_failed_requests(),
    )

    call_client = CallModelApi(
        "modelrun.inference.cloud.ru",
        options.with_autogenerated_request_id(),
        options.with_autogenerated_traceparent(),
        with_retries_failed_requests(),
    )

    call_client_with_auth = CallModelApi(
        "modelrun.inference.cloud.ru",
        options.with_auth(auth),
        options.with_autogenerated_request_id(),
        options.with_autogenerated_traceparent(),
        with_retries_failed_requests(),
    )

    return bff_client, call_client, call_client_with_auth, project_id


def print_json(obj):
    if hasattr(obj, "model_dump_json"):
        print(obj.model_dump_json(indent=2))
    elif hasattr(obj, "model_dump"):
        print(json.dumps(obj.model_dump(), indent=2, default=str))
    else:
        print(json.dumps(obj, indent=2, default=str))


def check_response(res, action: str):
    if not res.response.is_success:
        print(
            f"Error {action}: HTTP {res.response.status_code}",
            file=sys.stderr,
        )
        print(res.response.text, file=sys.stderr)
        sys.exit(1)


