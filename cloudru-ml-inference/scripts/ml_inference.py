def cmd_create(args):
    """Create a model run using raw JSON payload.

    The Cloud.ru API is strict about null values and requires specific field
    formats, so we build the payload as a dict and POST it directly via httpx
    instead of relying on SDK model serialization.
    """
    bff, _, _, project_id = build_clients()

    framework_enum_map = {
        "VLLM": "FrameworkType_VLLM",
        "SGLANG": "FrameworkType_SGLANG",
        "OLLAMA": "FrameworkType_OLLAMA",
        "TRANSFORMERS": "FrameworkType_TRANSFORMERS",
        "DIFFUSERS": "FrameworkType_DIFFUSERS",
        "COMFY": "FrameworkType_COMFY_UI",
    }
    resource_enum_map = {
        "GPU_A100": "ResourceType_GPU_A100_NVLINK",
        "GPU_H100": "ResourceType_GPU_H100",
        "GPU_V100": "ResourceType_GPU_V100",
        "CPU": "ResourceType_CPU",
    }
    task_enum_map = {t.name: t.value for t in ModelTaskType}

    fw = args.framework.upper()
    framework_val = framework_enum_map.get(fw)
    if not framework_val:
        print(f"Unknown framework: {args.framework}", file=sys.stderr)
        sys.exit(1)

    resource_val = resource_enum_map.get(args.resource.upper()) if args.resource else "ResourceType_GPU_A100_NVLINK"
    task_val = task_enum_map.get(args.task.upper()) if args.task else "ModelTaskType_GENERATE"

    # Build model source
    repo = args.repo or ""
    model_name = args.model_name
    if not model_name and "/" in repo:
        repo, model_name = repo.split("/", 1)

    if args.source_type == "huggingface":
        model_source = {
            "huggingFaceRepository": {
                "repo": repo,
                "model": model_name or "",
                "revision": args.revision or "main",
                "filePaths": [],
            },
            "secret": "",
            "repoSize": 0,
        }
    elif args.source_type == "ollama":
        model_source = {
            "ollama": {
                "model": model_name or repo,
                "repo": "",
                "revision": "",
            },
            "secret": "",
            "repoSize": 0,
        }
    elif args.source_type == "registry":
        model_source = {
            "modelRegistry": {
                "repo": repo,
                "model": model_name or "",
                "revision": args.revision or "",
            },
            "secret": "",
            "repoSize": 0,
        }
    elif args.source_type == "modelscope":
        model_source = {
            "modelScope": {
                "repo": repo,
                "model": model_name or "",
                "revision": args.revision or "",
            },
            "secret": "",
            "repoSize": 0,
        }
    else:
        model_source = {}

    # Build serving options — use dynamicalOptions for VLLM/SGLANG
    serving_options = {}
    if fw in ("VLLM", "SGLANG"):
        dyn_args = args.vllm_args if hasattr(args, "vllm_args") and args.vllm_args else []
        serving_options = {
            "dynamicalOptions": {
                "args": dyn_args,
                "loraModules": [],
            }
        }
    elif fw == "OLLAMA":
        serving_options = {"ollamaOptions": {}}
    elif fw == "TRANSFORMERS":
        serving_options = {"transformersOptions": {}}
    elif fw == "DIFFUSERS":
        serving_options = {"diffusersOptions": {}}

    # Build scaling (required)
    min_scale = args.min_scale if args.min_scale is not None else 1
    max_scale = args.max_scale if args.max_scale is not None else 1
    scaling = {
        "minScale": min_scale,
        "maxScale": max_scale,
        "scalingRules": {"rpsType": {"value": 200}},
    }

    # Resolve runtime template if not provided
    runtime_template_id = args.runtime_template_id
    if not runtime_template_id:
        # Auto-pick latest active runtime for the framework
        rt_res = bff.v1.get_framework_versions(
            project_id=project_id, params={"limit": 100, "offset": 0}
        )
        if rt_res.response.is_success:
            for rt in rt_res.model().runtime_templates:
                if rt.framework_type == framework_enum_map.get(fw) and rt.is_active:
                    runtime_template_id = rt.id
                    break
        if not runtime_template_id:
            print("Error: could not auto-detect runtime template. Pass --runtime-template-id.", file=sys.stderr)
            sys.exit(1)

    payload = {
        "name": args.name,
        "frameworkType": framework_val,
        "resourceType": resource_val,
        "gpuCount": args.gpu_count or 1,
        "gpuGbMemory": args.gpu_memory or 20,
        "modelTaskType": task_val,
        "runtimeTemplateId": runtime_template_id,
        "modelSource": model_source,
        "servingOptions": serving_options,
        "scaling": scaling,
        "options": {
            "isEnabledAuth": False,
            "isEnabledLogging": False,
        },
    }

    # POST directly via the underlying httpx client to avoid null serialization issues
    import httpx as _httpx

    response = bff.post(
        f"u-api/inference/model-run/v1/{project_id}/modelruns",
        json=payload,
    )
    if not response.is_success:
        print(f"Error creating model run: HTTP {response.status_code}", file=sys.stderr)
        print(response.text, file=sys.stderr)
        sys.exit(1)
    data = response.json()
    print(f"Created model run: {data.get('modelRunId', data)}")


def cmd_catalog(args):
    """List predefined models from the Cloud.ru catalog."""
    bff, _, _, _ = build_clients()
    params = {
        "limit": args.limit or 100,
        "offset": args.offset or 0,
    }
    if args.query:
        params["query"] = args.query
    if args.sort:
        params["sort"] = args.sort

    response = bff.get(
        "u-api/inference/model-run/v1/predefined-models",
        params=params,
    )
    if not response.is_success:
        print(f"Error: HTTP {response.status_code}", file=sys.stderr)
        print(response.text, file=sys.stderr)
        sys.exit(1)

    data = response.json()
    total = data.get("total", 0)
    has_more = data.get("hasMore", False)
    cards = data.get("modelCards", [])
    print(f"Predefined models ({len(cards)} shown, {total} total, hasMore={has_more}):\n")
    for c in cards:
        tags = ", ".join(c.get("tags", []))
        tags_str = f" [{tags}]" if tags else ""
        print(
            f"  {c['id']} | {c['name']:<40} | {c.get('vendorName',''):<10} "
            f"| {c.get('paramsBn','')}B | ctx={c.get('contextK','')}K "
            f"| {c.get('price','')} rub/hour{tags_str}"
        )


