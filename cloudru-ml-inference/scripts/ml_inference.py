def cmd_call(args):
    _, call_client, call_client_auth, project_id = build_clients()
    client = call_client_auth if args.with_auth else call_client
    model_run_id = UUID(args.model_run_id)

    messages = [ModelItemMessage(role="user", content=args.prompt)]
    if args.system:
        messages.insert(0, ModelItemMessage(role="system", content=args.system))

    if args.framework and args.framework.upper() in ("VLLM", "SGLANG"):
        payload = ModelGenerateInputRequestVllmSglang(
            model=args.model_name or "",
            messages=messages,
            temperature=args.temperature,
            top_p=args.top_p,
        )
        response_schema = ModelGenerateResponseVllm
    else:
        payload = ModelTextToTextGenerationInputsRequestsOllama(
            model=args.model_name or "",
            messages=messages,
            keep_alive=1,
        )
        response_schema = ModelTextToTextGenerationResponseOllama

    res = client.v1.call_model_run_with_compatible_openai(
        model_run_id=model_run_id,
        response_schema=response_schema,
        model_run_request_body_data=payload,
    )
    check_response(res, "calling model")
    result = res.model()
    if result.choices:
        print(result.choices[0].message.content)
    print(f"\nTokens: {result.usage.total_tokens}")


