def cmd_deploy(args):
    """Deploy a predefined model — fetches exact config from catalog and creates model run."""
    bff, _, _, project_id = build_clients()

    # 1. Fetch model card details
    response = bff.get(
        f"u-api/inference/model-run/v1/predefined-models/{args.model_card_id}",
    )
    if not response.is_success:
        print(f"Error fetching model card: HTTP {response.status_code}", file=sys.stderr)
        print(response.text, file=sys.stderr)
        sys.exit(1)

    data = response.json()
    card = data.get("modelCard", {})
    configs = data.get("modelCardConfigs", [])

    if not configs:
        print("Error: no configurations available for this model", file=sys.stderr)
        sys.exit(1)

    # 2. Pick config (by index or auto-pick first)
    config_index = args.config_index if args.config_index is not None else 0
    if config_index >= len(configs):
        print(f"Error: config index {config_index} out of range (0-{len(configs)-1})", file=sys.stderr)
        sys.exit(1)
    cfg = configs[config_index]

    model_name = args.name or card.get("name", "model-run")
    task_type = card.get("taskType", "ModelTaskType_GENERATE")

    # 3. Build payload from the predefined config — no guessing
    payload = {
        "name": model_name,
        "frameworkType": cfg["frameworkType"],
        "resourceType": cfg["allowedGpu"],
        "gpuCount": cfg["gpuCount"],
        "gpuGbMemory": cfg["gpuMemoryAllocGb"],
        "modelTaskType": task_type,
        "runtimeTemplateId": cfg["runtimeTemplateId"],
        "modelSource": card.get("modelSource", {}),
        "servingOptions": cfg.get("servingOptions", {}),
        "scaling": cfg.get("scaling", {"minScale": 1, "maxScale": 1, "scalingRules": {"rpsType": {"value": 200}}}),
        "options": {
            "isEnabledAuth": False,
            "isEnabledLogging": False,
        },
    }

    print(f"Deploying '{card.get('name', '?')}' with config [{config_index}]:")
    print(f"  GPU: {cfg.get('allowedGpu')} x{cfg.get('gpuCount')} ({cfg.get('gpuMemoryAllocGb')}GB)")
    print(f"  Framework: {cfg.get('frameworkType')} {cfg.get('frameworkVersion', '')}")
    print(f"  Price: {cfg.get('price', '?')} rub/day")

    # 4. Create model run
    create_resp = bff.post(
        f"u-api/inference/model-run/v1/{project_id}/modelruns",
        json=payload,
    )
    if not create_resp.is_success:
        print(f"\nError creating model run: HTTP {create_resp.status_code}", file=sys.stderr)
        print(create_resp.text, file=sys.stderr)
        sys.exit(1)

    result = create_resp.json()
    model_run_id = result.get("modelRunId", result)
    print(f"\nCreated model run: {model_run_id}")
    print(f"Public URL: {model_run_id}.modelrun.inference.cloud.ru")


def cmd_update(args):
    bff, _, _, project_id = build_clients()

    update_fields = {}
    if args.name:
        update_fields["name"] = args.name
    if args.min_scale is not None or args.max_scale is not None:
        update_fields["scaling"] = Scaling(
            min_scale=args.min_scale or 0,
            max_scale=args.max_scale or 1,
            scaling_rules=ScalingRulesConcurrencyType(
                concurrency_type=ConcurrencyType(soft=1, hard=2),
            ),
            keep_alive_duration=KeepAliveDuration(
                hours=0, minutes=args.keep_alive_minutes or 15, seconds=0
            ),
        )

    body = ModelRunCreateItem(**update_fields)
    res = bff.v1.update_model_run(
        project_id=project_id,
        model_run_id=UUID(args.model_run_id),
        model_run_body_data=body,
    )
    check_response(res, "updating model run")
    print("Updated successfully")


